SELECT 
    year,
    make,
    model,
    -- Replacing NULL body styles with 'unknown'
    COALESCE(body, 'unknown') AS body_cleaned,
    -- Replacing NULL transmission with 'not specified'
    COALESCE(transmission, 'not specified') AS transmission_cleaned,
    -- Replacing NULL odometer readings with 0
    COALESCE(odometer, 0) AS odometer_cleaned,
    -- Replacing NULL interior colors with the exterior color (if available)
    COALESCE(interior, color, 'unknown') AS interior_final
FROM `bright`.`casestudy`.`car_sales_data`;


